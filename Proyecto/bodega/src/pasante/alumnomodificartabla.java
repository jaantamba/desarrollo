    public void mostrar(String id){
        String sql="select * from alumno where id_alumno ='"+id+"'";
            Statement st;
    try {
        st = cn.createStatement();
        ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                idEstudiante=(String) rs.getObject(2);
                cedulaEstudiante=(String) rs.getObject(3);
                nombreEstudiante=(String) rs.getObject(4);
                apellidosEstudiante=(String) rs.getObject(5);
                correoEstudiante=(String) rs.getObject(6);
                nivelEstudiante=(String) rs.getObject(7);
                carreraEstudiante=(String) rs.getObject(8);
                periodoEstudiante=(String) rs.getObject(9);
                telefonoEstudiante=(String) rs.getObject(10);
                
                txtnombresEstudiante.setText(nombreEstudiante);
                txtidEstudiante.setText(idEstudiante);
                txtcedulaEstudiante.setText(cedulaEstudiante);
                txtapellidosEstudiante.setText(apellidosEstudiante);
                txtcorreoEstudiante.setText(correoEstudiante);
                txtnivelEstudiante.setText(nivelEstudiante);
                txtcarreraEstudiante1.setText(carreraEstudiante);
                txtperiodoEstudiante.setText(periodoEstudiante);
                txttelefonoEstudiante.setText(telefonoEstudiante);
            }
    } catch (SQLException ex) {
        Logger.getLogger(alumnomodificartabla.class.getName()).log(Level.SEVERE, null, ex);
    }
    }
