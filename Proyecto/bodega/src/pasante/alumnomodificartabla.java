    private void txtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGuardarActionPerformed
        // TODO add your handling code here:
        int opcionGuardado;
        String sql="UPDATE alumno SET id_alum_universidad = '"+txtidEstudiante.getText()+"',cedula_alumno='"+txtcedulaEstudiante.getText()+"',nombres_alumno = '"+txtnombresEstudiante.getText()+"',apellidos_alumno='"+txtapellidosEstudiante.getText()+"',correo_alumno='"+txtcorreoEstudiante.getText()+"',nivel_alumno='"+txtnivelEstudiante.getText()+"',carrera_alumno='"+txtcarreraEstudiante1.getText()+"',periodo_alumno='"+txtperiodoEstudiante.getText()+"',telefono_alumno='"+txttelefonoEstudiante.getText()+"' WHERE (id_alumno = '"+identificador+"');";
        try {
            
            Statement st = (Statement)cn.createStatement();
            opcionGuardado=JOptionPane.showConfirmDialog(null, "¿Esta seguro de guardar?", "Confirmar guardado", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (opcionGuardado==0) {
                JOptionPane.showMessageDialog(null,"Guardado");
                st.executeUpdate(sql);    
            }else{
                JOptionPane.showMessageDialog(null,"No Guardado");
            }
            
        
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage() ,"Error", 1);
        }
    }//GEN-LAST:event_txtGuardarActionPerformed

